// Helper to get a category by ID
export function getCategoryById(id) {
  return CATEGORIES.find(c => c.id === id);
}

// Helper to get an item from a category
export function getItemById(categoryId, itemId) {
  const cat = getCategoryById(categoryId);
  return cat?.items.find(i => i.id === itemId);
}

// Helper to get cooking method by ID
export function getMethodById(id) {
  return COOKING_METHODS.find(m => m.id === id);
}
